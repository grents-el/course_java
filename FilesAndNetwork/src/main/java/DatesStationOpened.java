

public class DatesStationOpened {
    private String stationName;
    private String date;

    public DatesStationOpened(String stationName, String date) {
        this.stationName = stationName;
        this.date = date;
    }

    public String getStationName() {
        return stationName;
    }

    public String getDate() {
        return date;
    }
}


